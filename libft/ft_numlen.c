int		ft_numlen(int nb)
{
	int			i;

	if (nb == 0)
		return (1);
	i = 0;
	nb < 0 ? nb = -nb : 0;
	while (nb > 0)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
